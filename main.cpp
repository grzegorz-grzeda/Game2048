#include "Game.hpp"
/*================================================================================================*/
using namespace std;
/*================================================================================================*/
int main() {
	cout << "Set the row count (3-10): ";
	cout.flush();
	int count;
	cin >> count;

	count = (count < 3) ? 3 : count;
	count = (count > 10) ? 10 : count;

	Game game(count);
	while (game.isGameNotOver()) {
		game.update();
	}
	return 0;
}
